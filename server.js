function parseNumberWithSuffix(input) {
    if (!input) return null;
    const str = String(input).toLowerCase().trim();
    const suffixesSorted = Object.keys(SUFFIXES).sort((a, b) => b.length - a.length);
    for (const suffix of suffixesSorted) {
        if (str.endsWith(suffix)) {
            const numStr = str.slice(0, -suffix.length);
            const value = parseFloat(numStr);
            if (!isNaN(value)) {
                const multiplier = SUFFIXES[suffix];
                const result = BigInt(Math.floor(value)) * multiplier;
                if (result > 10n ** 260n) return (10n ** 260n).toString();
                return result.toString();
            }
        }
    }
    const value = parseFloat(str);
    if (!isNaN(value)) {
        if (value > 1e260) return (10n ** 260n).toString();
        return BigInt(Math.floor(value)).toString();
    }
    return null;
}

